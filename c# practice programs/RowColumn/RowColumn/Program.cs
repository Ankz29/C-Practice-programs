using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace RowColumn
{
    class Program
    {
        static void Main(string[] args)
        {
            int n = 4;
            for (int i = 1; i <= n; i++)
            {
                for (int j = 1; j <= n; j++)
                {
                    Console.WriteLine("25");                    
                }
                Console.WriteLine(" ");
                Console.ReadLine();
            }
            
        }
    }
